    private void Start()
    {
        if (nodeParent == null) {
            nodeParent = gameObject.transform;
        }
        Vector3 position = gameObject.transform.position;
        for (int i = 0; i < gridSideLength * gridSideLength; i++) {
            position.x = towerSideLength * ((i % gridSideLength) + 1);
            position.z = towerSideLength * (gridSideLength - Mathf.Floor(((float)i) / gridSideLength));
            if (nodes[i] == 1)
            {
                position.y = 0f;
                GameObject node = (GameObject)Instantiate(towerNode, position, Quaternion.identity);
                node.transform.SetParent(nodeParent);
            }
            if (i % groundScale == 0 && (i / gridSideLength) % groundScale == 0)
            {
                position.y = -.199f + .00001f * i; //This allows the tiles to go over the plane used for navigation
                position.x -= 2f;
                GameObject ground = (GameObject)Instantiate(groundNode, position, Quaternion.identity);
                ground.transform.localScale = new Vector3(groundScale + groundScale * .01f, 1, groundScale);
                float colorModifier = Random.Range(0f, maxColorDarken);
                Renderer[] rends = ground.GetComponentsInChildren<Renderer>();
                foreach (Renderer rend in rends)
                {
                    rend.material.color = new Color(
                        (rend.material.color.r - colorModifier),
                        (rend.material.color.g - colorModifier),
                        (rend.material.color.b - colorModifier));
                }
                //if (Random.Range(0, 1) > .5)
                //{
                //    ground.transform.rotation = Quaternion.Euler(
                //        ground.transform.rotation.x,
                //        ground.transform.rotation.y + 90,
                //         ground.transform.rotation.z);
                //}

                ground.transform.SetParent(nodeParent);
            }

        }
        navMeshGround.BuildNavMesh();
    }
